class GarageDoor:
    def up(self):
        print("Garage door is up")

    def down(self):
        print("Garage door is down")

    def stop(self):
        print("Garage door is stopped")

    def light_on(self):
        print("Garage light is on")
    
    def light_off(self):
        print("Garage light is off")

    